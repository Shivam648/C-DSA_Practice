using namespace std;
#include <iostream>
int main(){
    int a,b,c;
    cout <<"Enter the value of a";
    cin >>a;
    cout <<"Enter the value of b";
    cin >>b;
    cout <<"Enter the value of c";
    cin >>c;
    cout << ++a;
    cout <<"\n";
    cout << ++b;
    cout <<"\n";
    cout << --c;
    cout <<"\n";
    return 0;
} 
