#include <iostream>
using namespace std;

int main()
{
    int a = 10, b = 20, c = 30, d = 40;
    if (a == b)
        cout << "a is equal to b" << endl;
    else
        cout << "a is not equal to b" << endl;
    if (c != d)
        cout << "c is not equal to d" << endl;
    else
        cout << "c is equal to d" << endl;
}
